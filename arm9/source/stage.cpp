int Stage::AllocateSlot()
{
    if (freeSlots.empty()) return -1;

    int slot = freeSlots.back();
    freeSlots.pop_back();
    return slot;
}

void Stage::FreeSlot(int slot)
{
    freeSlots.push_back(slot);
}

void Stage::Update(PlayerInput input)
{
    frame++;

    // ⭐⭐⭐ 죽은 적 완전 제거 (이게 핵심!)
    for (auto it = enemies.begin(); it != enemies.end(); ) {
        if (!(*it)->IsAlive() && (*it)->freed) {
            it = enemies.erase(it);   // vector에서 완전히 삭제
        } else {
            ++it;
        }
    }

    // ⭐ 이벤트 스폰 처리
    for(int i = 0; i < eventCount; i++)
    {
        if(events[i].frame == frame)
        {   
            if (freeSlots.empty()) {
                pendingSpawns.push_back(events[i]);
            } else {
                SpawnEnemy(events[i]);
            }
        }
    }

    // ⭐ pending spawn 처리
    for (auto it = pendingSpawns.begin(); it != pendingSpawns.end(); )
    {
        if (!freeSlots.empty()) {
            SpawnEnemy(*it);
            it = pendingSpawns.erase(it);
        } else {
            ++it;
        }
    }


    // ⭐ 적 업데이트
    for(auto &enemyPtr : enemies)
    {
        enemyPtr->Update({
            input.touchX,
            input.touchY,
            input.touching,
            input.shotX,
            input.shotY,
            input.shot, 
            input.playerX, 
            input.damage
        });
    }

}

bool Stage::IsClear()
{   
    if (frame < events[eventCount-1].frame)
        return false; 
    if (!pendingSpawns.empty())
        return false; 
        
    for(auto &enemy : enemies)
    {
        if(enemy->IsAlive())
            return false;
    }
    return true;
}

